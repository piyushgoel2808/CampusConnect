    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
      String name = JOptionPane.showInputDialog(this, "Enter Name:");
if (name == null || name.trim().isEmpty()) return;

String email = JOptionPane.showInputDialog(this, "Enter Email:");
if (email == null || email.trim().isEmpty()) return;

String username = JOptionPane.showInputDialog(this, "Enter Username:");
if (username == null || username.trim().isEmpty()) return;

String password = JOptionPane.showInputDialog(this, "Enter Password:");
if (password == null || password.trim().isEmpty()) return;

try (Connection conn = dbconnection.getConnection()) {

    String sql = "INSERT INTO users (name, email, username, password) VALUES (?, ?, ?, ?)";
    PreparedStatement ps = conn.prepareStatement(sql);

    ps.setString(1, name.trim());
    ps.setString(2, email.trim());
    ps.setString(3, username.trim());
    ps.setString(4, password.trim());

    int rows = ps.executeUpdate();

    if (rows > 0) {
        JOptionPane.showMessageDialog(this, "User Added Successfully!");
        loadUsersData();
    }

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error adding user: " + e.getMessage());
}

    }//GEN-LAST:event_btnAddActionPerformed
public void loadUsersData() {
    try (Connection conn = dbconnection.getConnection()) {

        String sql = "SELECT * FROM users";
        PreparedStatement ps = conn.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        while (rs.next()) {
            Object[] row = {
                rs.getInt("user_id"),
                rs.getString("name"),
                rs.getString("email"),
                rs.getString("username"),
                rs.getString("password")
            };
            model.addRow(row);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading users: " + e.getMessage());
    }
}


    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
      int selectedRow = jTable1.getSelectedRow();
if (selectedRow == -1) {
    JOptionPane.showMessageDialog(this, "Please select a row to edit.");
    return;
}

int userId = (int) jTable1.getValueAt(selectedRow, 0);
String currentName = (String) jTable1.getValueAt(selectedRow, 1);
String currentEmail = (String) jTable1.getValueAt(selectedRow, 2);
String currentUsername = (String) jTable1.getValueAt(selectedRow, 3);
String currentPassword = (String) jTable1.getValueAt(selectedRow, 4);

String newName = JOptionPane.showInputDialog(this, "Edit Name:", currentName);
if (newName == null) return;

String newEmail = JOptionPane.showInputDialog(this, "Edit Email:", currentEmail);
if (newEmail == null) return;

String newUsername = JOptionPane.showInputDialog(this, "Edit Username:", currentUsername);
if (newUsername == null) return;

String newPassword = JOptionPane.showInputDialog(this, "Edit Password:", currentPassword);
if (newPassword == null) return;

try (Connection conn = dbconnection.getConnection()) {

    String sql = "UPDATE users SET name = ?, email = ?, username = ?, password = ? WHERE user_id = ?";
    PreparedStatement ps = conn.prepareStatement(sql);

    ps.setString(1, newName.trim());
    ps.setString(2, newEmail.trim());
    ps.setString(3, newUsername.trim());
    ps.setString(4, newPassword.trim());
    ps.setInt(5, userId);

    int rows = ps.executeUpdate();

    if (rows > 0) {
        JOptionPane.showMessageDialog(this, "User Updated Successfully!");
        loadUsersData();
    }

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error updating user: " + e.getMessage());
}

    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    int selectedRow = jTable1.getSelectedRow();

if (selectedRow == -1) {
    JOptionPane.showMessageDialog(this, "Please select a row to delete.");
    return;
}

int userId = (int) jTable1.getValueAt(selectedRow, 0);
String selectedName = (String) jTable1.getValueAt(selectedRow, 1);

int confirm = JOptionPane.showConfirmDialog(
        this,
        "Are you sure you want to delete user: \"" + selectedName + "\"?",
        "Confirm Delete",
        JOptionPane.YES_NO_OPTION
);

if (confirm != JOptionPane.YES_OPTION) return;

try (Connection conn = dbconnection.getConnection()) {

    String sql = "DELETE FROM users WHERE user_id = ?";
    PreparedStatement ps = conn.prepareStatement(sql);
    ps.setInt(1, userId);

    int rowsDeleted = ps.executeUpdate();

    if (rowsDeleted > 0) {
        JOptionPane.showMessageDialog(this, "User deleted successfully!");
        loadUsersData();
    }

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error deleting user: " + e.getMessage());
}

    }//GEN-LAST:event_btnDeleteActionPerformed

