    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
     // TODO add your handling code here:
       String searchText = txtSearch.getText().trim();

if (searchText.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please enter a name, email, or username to search.");
    return;
}

try (Connection conn = dbconnection.getConnection()) {

    String sql = "SELECT * FROM users WHERE name LIKE ? OR email LIKE ? OR username LIKE ?";
    PreparedStatement ps = conn.prepareStatement(sql);

    ps.setString(1, "%" + searchText + "%");
    ps.setString(2, "%" + searchText + "%");
    ps.setString(3, "%" + searchText + "%");

    ResultSet rs = ps.executeQuery();

    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);  // clear table

    while (rs.next()) {
        Object[] row = {
            rs.getInt("user_id"),
            rs.getString("name"),
            rs.getString("email"),
            rs.getString("username"),
            rs.getString("password")
        };
        model.addRow(row);
    }

    if (model.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "No matching users found.");
    }

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error searching users: " + e.getMessage());
}

    }//GEN-LAST:event_btnSearchActionPerformed

