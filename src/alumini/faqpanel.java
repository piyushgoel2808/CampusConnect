    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
    String question = JOptionPane.showInputDialog(this, "Enter Question:");
if (question == null || question.trim().isEmpty()) return;

String answer = JOptionPane.showInputDialog(this, "Enter Answer:");
if (answer == null || answer.trim().isEmpty()) return;

String keywords = JOptionPane.showInputDialog(this, "Enter Keywords (comma separated):");
if (keywords == null || keywords.trim().isEmpty()) return;

try (Connection conn = dbconnection.getConnection()) {

    String sql = "INSERT INTO faqs (question, answer, keywqrds) VALUES (?, ?, ?)";
    PreparedStatement ps = conn.prepareStatement(sql);

    ps.setString(1, question.trim());
    ps.setString(2, answer.trim());
    ps.setString(3, keywords.trim());

    int rows = ps.executeUpdate();

    if (rows > 0) {
        JOptionPane.showMessageDialog(this, "FAQ Added Successfully!");
        loadFaqData();
    }

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error adding FAQ: " + e.getMessage());
}

    }//GEN-LAST:event_btnAddActionPerformed
public void loadFaqData() {
    try (Connection conn = dbconnection.getConnection()) {

        String sql = "SELECT * FROM faqs";
        PreparedStatement ps = conn.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        while (rs.next()) {
            Object[] row = {
                rs.getInt("faq_id"),
                rs.getString("question"),
                rs.getString("answer"),
                rs.getString("keywords")
            };
            model.addRow(row);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading FAQs: " + e.getMessage());
    }
}


    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
int selectedRow = jTable1.getSelectedRow();
if (selectedRow == -1) {
    JOptionPane.showMessageDialog(this, "Please select a row to edit.");
    return;
}

int faqId = (int) jTable1.getValueAt(selectedRow, 0);
String currentQ = (String) jTable1.getValueAt(selectedRow, 1);
String currentA = (String) jTable1.getValueAt(selectedRow, 2);
String currentK = (String) jTable1.getValueAt(selectedRow, 3);

// Ask new values
String newQ = JOptionPane.showInputDialog(this, "Edit Question:", currentQ);
if (newQ == null) return;

String newA = JOptionPane.showInputDialog(this, "Edit Answer:", currentA);
if (newA == null) return;

String newK = JOptionPane.showInputDialog(this, "Edit Keywords:", currentK);
if (newK == null) return;

try (Connection conn = dbconnection.getConnection()) {

    String sql = "UPDATE faqs SET question = ?, answer = ?, keywqrds = ? WHERE faq_id = ?";
    PreparedStatement ps = conn.prepareStatement(sql);

    ps.setString(1, newQ.trim());
    ps.setString(2, newA.trim());
    ps.setString(3, newK.trim());
    ps.setInt(4, faqId);

    int rows = ps.executeUpdate();

    if (rows > 0) {
        JOptionPane.showMessageDialog(this, "FAQ updated successfully!");
        loadFaqData();
    }

    } catch (Exception e) {
     JOptionPane.showMessageDialog(this, "Error updating FAQ: " + e.getMessage());
     }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    int selectedRow = jTable1.getSelectedRow();

if (selectedRow == -1) {
    JOptionPane.showMessageDialog(this, "Please select a row to delete.");
    return;
}

// Get ID and Question from selected row
int faqId = (int) jTable1.getValueAt(selectedRow, 0);
String selectedQuestion = (String) jTable1.getValueAt(selectedRow, 1);

// Confirmation
int confirm = JOptionPane.showConfirmDialog(
        this,
        "Are you sure you want to delete FAQ: \"" + selectedQuestion + "\"?",
        "Confirm Delete",
        JOptionPane.YES_NO_OPTION
);

if (confirm != JOptionPane.YES_OPTION) return;

try (Connection conn = dbconnection.getConnection()) {

    String sql = "DELETE FROM faqs WHERE faq_id = ?";
    PreparedStatement ps = conn.prepareStatement(sql);
    ps.setInt(1, faqId);

    int rowsDeleted = ps.executeUpdate();

    if (rowsDeleted > 0) {
        JOptionPane.showMessageDialog(this, "FAQ deleted successfully!");
        loadFaqData(); // Refresh table
    }

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error deleting FAQ: " + e.getMessage());
}
    }//GEN-LAST:event_btnDeleteActionPerformed

