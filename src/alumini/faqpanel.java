    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
     // TODO add your handling code here:
String searchText = txtSearch.getText().trim();

if (searchText.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please enter a keyword or question to search.");
    return;
}

try (Connection conn = dbconnection.getConnection()) {

    String sql = "SELECT * FROM faqs WHERE question LIKE ? OR answer LIKE ? OR keywords LIKE ?";
    PreparedStatement ps = conn.prepareStatement(sql);

    ps.setString(1, "%" + searchText + "%");
    ps.setString(2, "%" + searchText + "%");
    ps.setString(3, "%" + searchText + "%");

    ResultSet rs = ps.executeQuery();

    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);  // clear previous rows

    while (rs.next()) {
        Object[] row = {
            rs.getInt("faq_id"),
            rs.getString("question"),
            rs.getString("answer"),
            rs.getString("keywords")
        };
        model.addRow(row);
    }

    if (model.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "No matching FAQs found.");
    }

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error searching FAQs: " + e.getMessage());
}
    }//GEN-LAST:event_btnSearchActionPerformed

