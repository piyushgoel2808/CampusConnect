    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
     // TODO add your handling code here:
String searchText = txtSearch.getText().trim();

if (searchText.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please enter a job title to search.");
    return;
}

try (Connection conn = dbconnection.getConnection()) {

    String sql = "SELECT * FROM job_opportunities WHERE title LIKE ?";
    PreparedStatement ps = conn.prepareStatement(sql);
    ps.setString(1, "%" + searchText + "%");  // partial match on job title

    ResultSet rs = ps.executeQuery();
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // clear old rows

    while (rs.next()) {
        Object[] row = {
            rs.getInt("job_id"),
            rs.getString("title"),
            rs.getString("company"),
            rs.getString("location"),
            rs.getString("type"),
            rs.getString("department"),
            rs.getString("description"),
            rs.getDate("posted_date"),
            rs.getString("posted_by")
        };
        model.addRow(row);
    }

    if (model.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "No matching job opportunities found.");
    }

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error searching job opportunities: " + e.getMessage());
}
    }//GEN-LAST:event_btnSearchActionPerformed

