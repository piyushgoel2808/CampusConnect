try (Connection conn = dbconnection.getConnection()) {

    String sql = "INSERT INTO job_opportunities (title, company, location, type, department, description, posted_date, posted_by) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
    PreparedStatement ps = conn.prepareStatement(sql);

    ps.setString(1, title.trim());
    ps.setString(2, company.trim());
    ps.setString(3, location.trim());
    ps.setString(4, type.trim());
    ps.setString(5, department.trim());
    ps.setString(6, description.trim());
    ps.setDate(7, postedDate);
    ps.setString(8, postedBy.trim());

    int rowsInserted = ps.executeUpdate();

    if (rowsInserted > 0) {
        JOptionPane.showMessageDialog(this, "Job Opportunity Added Successfully!");
        loadJobData(); // refresh table
    }

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error adding job opportunity: " + e.getMessage());
}
   

