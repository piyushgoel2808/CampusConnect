    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
     String title = JOptionPane.showInputDialog(this, "Enter Announcement Title:");
    if (title == null || title.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Title is required.");
        return;
    }

    String description = JOptionPane.showInputDialog(this, "Enter Description:");
    if (description == null || description.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Description is required.");
        return;
    }

    String postedBy = JOptionPane.showInputDialog(this, "Posted By:");
    if (postedBy == null || postedBy.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Posted By is required.");
        return;
    }

    // Auto-set date_posted to current date
    java.sql.Date datePosted = new java.sql.Date(System.currentTimeMillis());

    try (Connection conn = dbconnection.getConnection()) {

        String sql = "INSERT INTO announcements (title, description, date_posted, posted_by) VALUES (?, ?, ?, ?)";
        PreparedStatement ps = conn.prepareStatement(sql);

        ps.setString(1, title.trim());
        ps.setString(2, description.trim());
        ps.setDate(3, datePosted);
        ps.setString(4, postedBy.trim());

        int rowsInserted = ps.executeUpdate();

        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(this, "Announcement Added Successfully!");
            loadAnnouncementsData(); // refresh table
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error adding announcement: " + e.getMessage());
    }
    }//GEN-LAST:event_btnAddActionPerformed

    private void loadAnnouncementsData() {
    try (Connection conn = dbconnection.getConnection()) {
        String sql = "SELECT * FROM announcements";
        PreparedStatement ps = conn.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("announcement_id"),
                rs.getString("title"),
                rs.getString("description"),
                rs.getDate("date_posted"),
                rs.getString("posted_by")
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading announcements: " + e.getMessage());
    }
}

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
    int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a row to edit.");
        return;
    }

    // Get current values from table
    int announcementId = (int) jTable1.getValueAt(selectedRow, 0);
    String currentTitle = (String) jTable1.getValueAt(selectedRow, 1);
    String currentDescription = (String) jTable1.getValueAt(selectedRow, 2);
    String currentPostedBy = (String) jTable1.getValueAt(selectedRow, 4);

    // Ask for new values
    String newTitle = JOptionPane.showInputDialog(this, "Edit Title:", currentTitle);
    if (newTitle == null) return;

    String newDescription = JOptionPane.showInputDialog(this, "Edit Description:", currentDescription);
    if (newDescription == null) return;

    String newPostedBy = JOptionPane.showInputDialog(this, "Edit Posted By:", currentPostedBy);
    if (newPostedBy == null) return;

    try (Connection conn = dbconnection.getConnection()) {

        String sql = "UPDATE announcements SET title = ?, description = ?, posted_by = ? WHERE announcement_id = ?";
        PreparedStatement ps = conn.prepareStatement(sql);

        ps.setString(1, newTitle.trim());
        ps.setString(2, newDescription.trim());
        ps.setString(3, newPostedBy.trim());
        ps.setInt(4, announcementId);

        int rowsUpdated = ps.executeUpdate();

        if (rowsUpdated > 0) {
            JOptionPane.showMessageDialog(this, "Announcement Updated Successfully!");
            loadAnnouncementsData();  // Refresh table
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error updating announcement: " + e.getMessage());
    }
     // TODO add your handling code here:
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a row to delete.");
        return;
    }

    // Get ID and Title from selected row
    int announcementId = (int) jTable1.getValueAt(selectedRow, 0);
    String selectedTitle = (String) jTable1.getValueAt(selectedRow, 1);

    // Confirmation dialog
    int confirm = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to delete announcement: \"" + selectedTitle + "\"?",
            "Confirm Delete",
            JOptionPane.YES_NO_OPTION
    );

    if (confirm != JOptionPane.YES_OPTION) {
        return; // User clicked No
    }

    try (Connection conn = dbconnection.getConnection()) {

        String sql = "DELETE FROM announcements WHERE announcement_id = ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setInt(1, announcementId);

        int rowsDeleted = ps.executeUpdate();

        if (rowsDeleted > 0) {
            JOptionPane.showMessageDialog(this, "Announcement deleted successfully!");
            loadAnnouncementsData(); // Refresh table
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error deleting announcement: " + e.getMessage());
    }      // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteActionPerformed

