    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
     // TODO add your handling code here:
     String searchText = txtSearch.getText().trim();

    if (searchText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a title to search.");
        return;
    }

    try (Connection conn = dbconnection.getConnection()) {
        String sql = "SELECT * FROM announcements WHERE title LIKE ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, "%" + searchText + "%");  // partial search by title

        ResultSet rs = ps.executeQuery();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // clear table before adding new rows

        while (rs.next()) {
            Object[] row = {
                rs.getInt("announcement_id"),
                rs.getString("title"),
                rs.getString("description"),
                rs.getDate("date_posted"),
                rs.getString("posted_by")
            };
            model.addRow(row);
        }

        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "No announcements found.");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error searching announcements: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSearchActionPerformed

